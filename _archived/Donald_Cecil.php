<p class="spacing">
<strong>Donald Cecil</strong> was a founding partner in 1970 of Cumberland Associates, a private investment management firm with more than $1 billion in assets under management. He was managing partner until 1982 when he became a special limited partner in order to devote more time to civic, charitable, and corporate activities.
From 1963 to 1970 he was senior vice president and director of institutional research at Shearson Hammill (now known as Morgan Stanley Smith Barney) and president of Shearson Hammill Asset Management Company.</p>
<p class="spacing">Mr. Cecil is a member of the Economic Club of New York, the Institute of Chartered Financial Analysts, and the New York Society of Security Analysts. He is a retired director and trustee of 43 Merrill Lynch Mutual Funds domestic and international; a member of Rycote Advisory Panel, Geneva, Switzerland; retired chairman of the Valuation Advisory Board of Biotechnology Investments Ltd., London; former chairman of the Directors Program Committee of the Investment Company Institute, Washington, D.C.; and a former director of Grey Advertising, Columbus Dental, and Ward Foods.</p>
