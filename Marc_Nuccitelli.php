<strong>Marc Nuccitelli</strong> is Managing Partner and Co-Founder of Balfour Partners.  Balfour Partners is a private equity platform primarily engaged in investing in distressed companies.  Mr. Nuccitelli has nearly 20 years of aggregate distressed investing experience.  He is currently on the Board of Directors for American Media Inc.
Most recently Mr. Nuccitelli was the Portfolio Manager for the Credit Opportunities Fund at Brencourt Advisors.  He managed approximately 50% of the firm's assets which peaked at $2.5 billion. Investments included corporate bank debt, bonds, and equities.  He specialized in distressed and stressed special situations, in which a company may need to go through an operational or balance sheet restructuring.  He worked collaboratively with management to achieve these goals and has been actively involved on bond and equity committees in many bankruptcy situations.
Prior to Brencourt, Mr. Nuccitelli was a Senior Vice President and Head Trader at Avenue Capital Management, a multi billion dollar alternative asset manager, specializing in private equity distressed investments.  His responsibilities included trading all asset classes, overseeing the trading desk, managing the investment process, and co-managing a high yield portfolio.
Prior to Avenue, Mr. Nuccitelli was a Principal at Banc of America Securities, in the Global Special Situations Group, where he assisted in managing $400mm in proprietary capital and was responsible for sales and trading of all assets classes, primarily focused on distressed and stressed credit special situations.
Mr. Nuccitelli began his career in 1993 at Miller Tabak Roberts Securities, where he was a Senior Vice President in corporate bond sales specializing in distressed, high yield, and high grade corporate bonds, converts, equities, and emerging markets debt.   
Mr. Nuccitelli was formerly on the Board of Directors of Werner Ladder.
Mr. Nuccitelli holds a Bachelor of Science in Finance from Syracuse University.
